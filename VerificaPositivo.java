public class VerificaPositivo{
    public static void main(String[] args) {

        double numero  = 10;
       
        if(numero <  0){
            System.out.println("negativo");
        }if(numero > 0){
            System.out.println("positivo");
 
        }else{
            System.out.println("negativo");

        }


    }

}